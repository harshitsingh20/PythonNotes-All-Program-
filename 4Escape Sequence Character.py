# List of Escape Sequence character

# Escape Sequence character in Python is a sequence of characters that represents a single character
# It doesn't represent itself when used inside string literal or character.

#1) \n :- To add a new line

print("Harshit\nSingh")

#2) \t :- To add a new tab

print("Ankit Singh\t1")

#3) \' :- To add a single quot

print("It\'s all right")

#4) \\ :- To add a Single backslash

print("Anjani\\Singh")

#5) \r :- ASCII Carrage Return

print("Renu\rSingh")

#6) \":- To add the double quot

print("Shubhangi\"Singh")

#7) \a :- To add 

print("Simran\aSingh")

#8) \b :- It remove the closest word in\b

print("Sherya\bSingh")

#9) \f :- Form Feed to add the BEL

print("Tanu\fSingh")

#10) \v :- To add 

print("Nidhi\vSingh")

